/*
 * convert_GAMMA_to_V : convert CA gamma reg to V format
 * src : packed gamma value (CAh)
 * dst : extened V values (V255 ~ VT)
 */
static void convert_GAMMA_to_V(unsigned char* src, unsigned int *dst)
{
	/* i : V index
	 * j : RGB index
	 * k : extend V index
	 */
	int i,j,k = 0;

	for (i = V255; i >= VT; i--) {
		for (j = R; j < RGB_MAX; j++) {
			if (i == V255) {
				if (j == R) { /* k = 0 */
					dst[k] |= GET_BITS(src[0], 4, 5) << 8;
					dst[k] |= GET_BITS(src[1], 0, 7);
				} else if (j == G) { /* k = 1 */
					dst[k] |= GET_BITS(src[0], 2, 3) << 8;
					dst[k] |= GET_BITS(src[2], 0, 7);	
				} else if (j == B) { /* k = 2 */
					dst[k] |= GET_BITS(src[0], 0, 1) << 8;
					dst[k] |= GET_BITS(src[3], 0, 7);
				}
			} else if (i == V0) {
				if (j == R)  /* k = 30 */
					dst[k] |= GET_BITS(src[31], 4, 7);
				else if (j == G) /* k = 31 */
					dst[k] |= GET_BITS(src[31], 0, 3);
				else if (j == B) /* k = 32 */
					dst[k] |= GET_BITS(src[32], 4, 7);
			} else if (i == VT) {
				if (j == R) /* k = 33 */
					dst[k] |= GET_BITS(src[32], 0, 3);
				else if (j == G) /* k = 34 */
					dst[k] |= GET_BITS(src[33], 4, 7);
				else if (j == B) /* k = 35 */
					dst[k] |= GET_BITS(src[33], 0, 3);
			} else { /* V203 ~ V1, k = 3 ~ 29 */
				dst[k] = GET_BITS(src[k + 1], 0, 7);
			}

			k++;
		}
	}

	LCD_DEBUG("src[0~3] = %02x %02x %02x %02x\n", src[0], src[1], src[2], src[3]);
	LCD_DEBUG("src[31~33] = %02x %02x %02x \n", src[31], src[32], src[33]);

	return;
}

/*
 * convert_V_to_GAMMA : convert V format to CAh gamma reg
 * src : extened V values (V255 ~ VT)
 * dst : packed gamma values (CAh)
 */
static void convert_V_to_GAMMA(unsigned int *src, unsigned char* dst)
{
	/* i : gamma index
	 * k : packed gamma index
	 */
	int i,k = 0;

	for (i = 0; i < GAMMA_SET_MAX; i++) {
		if (i == 0) {
			dst[i] |= GET_BITS(src[k], 8, 9) << 4;
			dst[i] |= GET_BITS(src[k+1], 8, 9) << 2;
			dst[i] |= GET_BITS(src[k+2], 8, 9);
			
		} else if (i >= 31) {
			dst[i] |= GET_BITS(src[k++], 0, 4) << 4;
			dst[i] |= GET_BITS(src[k++], 0, 4);
		} else {
			dst[i] |= GET_BITS(src[k++], 0, 7);
		}
	}

	LCD_DEBUG("src[0~2] = %02x %02x %02x \n", src[0], src[1], src[2]);
	LCD_DEBUG("dst[0] = %02x \n", dst[0]);

	return;
}

void gen_hbm_interpolation_gamma_S6E3HA9_AMF670UH01(struct samsung_display_driver_data *vdd,
		struct ss_interpolation_brightness_table *normal_table, int normal_table_size)
{
	struct ss_interpolation *ss_itp;

	unsigned char *normal_max_gamma = vdd->panel_br_info.normal.gamma[0];
	unsigned char **hbm_itp_gamma;
	int **hbm_temp_gamma;
	unsigned char *hbm_max_gamma = vdd->panel_br_info.hbm_read_buffer;
	int normal_max_cd;

	int hbm_itp_step;
	int hbm_max_cd;
	int *hbm_itp_cd;	

	int gamma_size = vdd->dtsi_data.gamma_size;
	int extend_gamma_size = V_MAX * RGB_MAX;

	int *extend_normal_max_gamma;
	int *extend_hbm_max_gamma;

	int i, j;
	
	if (vdd->panel_br_info.itp_mode == FLASH_INTERPOLATION)
		ss_itp = &vdd->flash_itp;
	else
		ss_itp = &vdd->table_itp;

	hbm_itp_gamma = ss_itp->hbm.gamma;

	normal_max_cd = ss_itp->normal.br_aor_table[ss_itp->normal.brightness_step - 1].lux_mode;

	hbm_itp_step = ss_itp->hbm.brightness_step;
	hbm_max_cd = ss_itp->hbm.br_table[hbm_itp_step - 1].lux_mode;

	extend_normal_max_gamma = kzalloc(extend_gamma_size * sizeof(int), GFP_KERNEL);
	if (!extend_normal_max_gamma) {
		LCD_ERR("fail to alloc extend_normal_max_gamma %d\n", __LINE__);
		return;
	}

	extend_hbm_max_gamma = kzalloc(extend_gamma_size * sizeof(int), GFP_KERNEL);
	if (!extend_hbm_max_gamma) {
		LCD_ERR("fail to alloc extend_hbm_max_gamma %d\n", __LINE__);
		goto alloc_fail1;
	}

	/* alloc 2 dimenstion matrix */
	hbm_temp_gamma = kzalloc(hbm_itp_step * sizeof(void *), GFP_KERNEL);
	if (!hbm_temp_gamma) {
		LCD_ERR("fail to alloc hbm_temp_gamma %d\n", __LINE__);
		goto alloc_fail2;
	}

	for (i = 0; i < hbm_itp_step; i++) {
		hbm_temp_gamma[i] = kzalloc(extend_gamma_size * sizeof(int), GFP_KERNEL);

		if (!hbm_temp_gamma[i]) {
			LCD_ERR("fail to alloc	hbm_temp_gamma %d\n", __LINE__);
			goto alloc_fail3;
		}
	}

	hbm_itp_cd = kzalloc(hbm_itp_step * sizeof(int), GFP_KERNEL);
	if (!hbm_itp_cd) {
		LCD_ERR("fail to alloc hbm_itp_cd %d\n", __LINE__);
		goto alloc_fail3;
	}

	for (i = 0; i < hbm_itp_step; i++) {
		hbm_itp_cd[i] = ss_itp->hbm.br_table[i].interpolation_br_x10000;
		LCD_DEBUG("%d candela : %d\n", i, hbm_itp_cd[i]);
	}

	LCD_ERR("gamma_size %d / extend_gamma_size %d \n", gamma_size, extend_gamma_size);

	/* 1. Make V format from GAMMA format */
	convert_GAMMA_to_V(normal_max_gamma, extend_normal_max_gamma);
	convert_GAMMA_to_V(hbm_max_gamma, extend_hbm_max_gamma);

	for (i = 0; i < extend_gamma_size; i++)
		LCD_DEBUG("[%d] 0x%x 0x%x\n", i, extend_hbm_max_gamma[i], extend_normal_max_gamma[i]);

	/*
	 * 2. HBM GAMMA Interpolation. (V format base)
	 */
	for (i = 0 ; i < hbm_itp_step; i++) {
		for (j = 0; j < extend_gamma_size; j++) {
			hbm_temp_gamma[i][j] = 
				gamma_interpolation(extend_hbm_max_gamma[j], extend_normal_max_gamma[j], hbm_max_cd, normal_max_cd, hbm_itp_cd[i]);
		}
	}

	/* update max hbm gamma with origin hbm gamma */
	for (i = 0; i < extend_gamma_size; i++)
		hbm_temp_gamma[hbm_itp_step - 1][i] = extend_hbm_max_gamma[i];

	/* 3. Make GAMMA format from V format */
	for (i = 0; i < hbm_itp_step; i++)
		convert_V_to_GAMMA(hbm_temp_gamma[i], hbm_itp_gamma[i]);

	kfree(hbm_itp_cd);

alloc_fail3:
	for (i = 0; i < hbm_itp_step; i++)
		kfree(hbm_temp_gamma[i]);
	kfree(hbm_temp_gamma);
alloc_fail2:
	kfree(extend_hbm_max_gamma);
alloc_fail1:
	kfree(extend_normal_max_gamma);
}

void gen_hbm_interpolation_irc_S6E3HA9_AMF670UH01(struct samsung_display_driver_data *vdd,
		struct ss_interpolation_brightness_table *hbml_table, int hbm_table_size)
{
	int loop;
	struct ss_hbm_interpolation *hbm_itp;
	int hbm_interpolation_step;

	unsigned int irc_size = vdd->dtsi_data.irc_size;
	unsigned char *normal_max_candela_irc = vdd->panel_br_info.normal.irc[0]; /* flash data write oder */

	unsigned char **dest_irc;
	unsigned long long normal_max_br = vdd->panel_br_info.normal.candela_table[0]; /* flash data write oder */
	unsigned int hbm_max_br = vdd->panel_br_info.hbm.candela_table[0]; /* flash data write oder */

	unsigned long long cur_br_x10000, color, mtp;
	int reg_idx, irc;
	unsigned long long val1, val2, val3, val4, val5, res;

	if (vdd->panel_br_info.itp_mode == FLASH_INTERPOLATION)
		hbm_itp = &vdd->flash_itp.hbm;
	else
		hbm_itp = &vdd->table_itp.hbm;

	hbm_interpolation_step = hbm_itp->brightness_step;
	dest_irc = hbm_itp->irc;

	LCD_DEBUG("hbm_interpolation_step : %d \n", hbm_interpolation_step);

	for (loop = 0; loop < hbm_interpolation_step; loop++) {
		/* copy default irc string */
		memcpy(dest_irc[loop], normal_max_candela_irc, irc_size);

		cur_br_x10000 = hbm_itp->br_table[loop].interpolation_br_x10000;

		for (mtp = IRC_0_V1; mtp < IRC_V1_MAX; mtp++) {
			for (color = RED_ORDER; color < COLOR_ORDER_MAX; color++) {

				reg_idx = IRC_START_OFFSET + (COLOR_ORDER_MAX * mtp) + color;
				irc = (int)normal_max_candela_irc[reg_idx];

				/*
				 * ROUND(current_br / normal_max_br) * irc * (1 + (0.58 - 1) / (800 - 420) * (current_br - 420)), 0)
				 */

				val1 = (cur_br_x10000 * irc) / normal_max_br;
				val2 = cur_br_x10000 - (normal_max_br * MULTIPLY_x10000);
				val3 = val2 * 42 / 100;
				val4 = val3 * MULTIPLY_x100 / (hbm_max_br - normal_max_br);
				val5 = 1 * MULTIPLY_x10000 * MULTIPLY_x100 - val4;

				res = val1 * val5 / MULTIPLY_x10000 / MULTIPLY_x10000;
				res = ROUNDING(res) / MULTIPLY_x100;

				dest_irc[loop][reg_idx] = res;

				LCD_DEBUG("[%d][%d] %x(%d) %d %d %d => %x(%d)\n", loop, reg_idx,
					irc, irc, cur_br_x10000, normal_max_br, hbm_max_br, dest_irc[loop][reg_idx], dest_irc[loop][reg_idx]);
			}
		}
	}
}

